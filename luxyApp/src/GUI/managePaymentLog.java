    //Method to verify fields
    public boolean verify()
    {
        if (jTextField_LeasingID.getText().equals("")
                || jTextField_Status.getText().equals("")
                || jDate_PaymentDate.getDate() == null
                || jTextField_Type.getText().equals("")
                || jTextField_ID.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "One or More Empty Field(s).");
            return false;
        } else if (jDate_PaymentDate.getDate().compareTo(new Date()) > 0)
        {
            JOptionPane.showMessageDialog(null, "Invalid date.");
            return false;
        } else
        {
            return true;
        }
    }
    //Method to remove entry
    private void jButton_RemoveActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jButton_RemoveActionPerformed
    {//GEN-HEADEREND:event_jButton_RemoveActionPerformed
        //Verify selection
        if (jTextField_ID.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "No Payment Log Selected.");
        } else
        {
            int id = Integer.valueOf(jTextField_ID.getText());
            //Delete data
            pl.insertUpdateDeletePaymentLog('d', id, null, null, null, null, null,
                    null);
            pl.fillPaymentLogJtable(jTable, "");
            
            //Refresh table
            jTable.setModel(new DefaultTableModel(null, new Object[]
            {
                "ID", "Leasing ID", "Status", "Date", "Type", "Check Num",
                "Transaction"
            }));
            pl.fillPaymentLogJtable(jTable, jTextField_Search.getText());
            
            //Set text of fields after delete
            jTextField_ID.setText("");
            jTextField_LeasingID.setText("");
            jTextField_Status.setText("");
            jDate_PaymentDate.setDate(null);
            jTextField_Type.setText("");
            jTextField_Check.setText("");
            jTextField_Transact.setText("");
            jTextField_Search.setText("");
        }
    }//GEN-LAST:event_jButton_RemoveActionPerformed
    //Method to edit entry
    private void jButton_EditActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jButton_EditActionPerformed
    {//GEN-HEADEREND:event_jButton_EditActionPerformed
        //Get text of fields
        String leasingid = jTextField_LeasingID.getText();
        String status = jTextField_Status.getText();
        String type = jTextField_Type.getText();
        String check = jTextField_Check.getText();
        String transact = jTextField_Transact.getText();
        int id = Integer.valueOf(jTextField_ID.getText());
        
        //Verify selection
        if (verify())
        {
            //Convert date
            SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            String date = dateFormat.format(jDate_PaymentDate.getDate());
            
            //Update data
            if ("".equals(check) && "".equals(transact))
            {
                pl.insertUpdateDeletePaymentLog('u', id, leasingid, status, date,
                        type, null, null);
            } else if (!"".equals(check) && "".equals(transact))
            {
                pl.insertUpdateDeletePaymentLog('u', id, leasingid, status, date,
                        type, check, null);
            } else if ("".equals(check) && !"".equals(transact))
            {
                pl.insertUpdateDeletePaymentLog('u', id, leasingid, status, date,
                        type, null, transact);
            } else
            {
                pl.insertUpdateDeletePaymentLog('u', id, leasingid, status, date,
                        type, check, transact);
            }
            
            //Set text of fields after update
            jTextField_ID.setText("");
            jTextField_LeasingID.setText("");
            jTextField_Status.setText("");
            jDate_PaymentDate.setDate(null);
            jTextField_Type.setText("");
            jTextField_Check.setText("");
            jTextField_Transact.setText("");
            jTextField_Search.setText("");

            //Refresh table
            jTable.setModel(new DefaultTableModel(null, new Object[]
            {
                "ID", "Leasing ID", "Status", "Date", "Type", "Check Num",
                "Transaction"
            }));
            pl.fillPaymentLogJtable(jTable, "");
        }
    }//GEN-LAST:event_jButton_EditActionPerformed
