    //Method to verify field
    public boolean verify()
    {
        if (jTextField_FName.getText().equals("")
                || jTextField_LName.getText().equals("")
                || jTextField_IDProof.getText().equals("")
                || jTextArea_Address.getText().equals("")
                || jTextField_ID.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "One or More Empty Field(s).");
            return false;
        } else
        {
            return true;
        }
    }
    //Method to edit entry
    private void jButton_EditActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jButton_EditActionPerformed
    {//GEN-HEADEREND:event_jButton_EditActionPerformed
        //Get text of fields
        String fname = jTextField_FName.getText();
        String lname = jTextField_LName.getText();
        String idproof = jTextField_IDProof.getText();
        String address = jTextArea_Address.getText();
        int id = Integer.valueOf(jTextField_ID.getText());
        
        //Verify selection
        if (verify())
        {
            //Update data
            r.insertUpdateDeleteRenter('u', id, fname, lname, idproof, address);
            //Set text of fields after update
            jTextField_ID.setText("");
            jTextField_FName.setText("");
            jTextField_LName.setText("");
            jTextField_IDProof.setText("");
            jTextArea_Address.setText("");
            jTextField_Search.setText("");

            //Refresh table
            manageRenter.jTable.setModel(new DefaultTableModel(null, new Object[]
            {
                "ID", "First Name", "Last Name", "Identity Proof", "Address"
            }));
            r.fillRenterJtable(manageRenter.jTable, "");
        }
    }//GEN-LAST:event_jButton_EditActionPerformed
    //Method to remove entry
    private void jButton_RemoveActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jButton_RemoveActionPerformed
    {//GEN-HEADEREND:event_jButton_RemoveActionPerformed
        //Verify selection
        if (jTextField_ID.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "No Renter Selected.");
        } else
        {
            int id = Integer.valueOf(jTextField_ID.getText());
            //Delete data
            r.insertUpdateDeleteRenter('d', id, null, null, null, null);
            r.fillRenterJtable(jTable, "");
            //Refresh table
            jTable.setModel(new DefaultTableModel(null, new Object[]
            {
                "ID", "First Name", "Last Name", "Identity Proof", "Address"
            }));
            r.fillRenterJtable(jTable, jTextField_Search.getText());
            
            //Set text of fields after delete
            jTextField_ID.setText("");
            jTextField_FName.setText("");
            jTextField_LName.setText("");
            jTextField_IDProof.setText("");
            jTextArea_Address.setText("");
            jTextField_Search.setText("");
        }
    }//GEN-LAST:event_jButton_RemoveActionPerformed
