    //Method to fill fields if entry clicked
    private void jTableMouseClicked(java.awt.event.MouseEvent evt)//GEN-FIRST:event_jTableMouseClicked
    {//GEN-HEADEREND:event_jTableMouseClicked
        model = (DefaultTableModel) jTable.getModel();

        rowIndex = jTable.getSelectedRow();
        //Get info of entry
        jTextField_ID.setText(model.getValueAt(rowIndex, 0).toString());
        jTextField_FName.setText(model.getValueAt(rowIndex, 1).toString());
        jTextField_LName.setText(model.getValueAt(rowIndex, 2).toString());
        jTextField_IDProof.setText(model.getValueAt(rowIndex, 3).toString());
        jTextArea_Address.setText(model.getValueAt(rowIndex, 4).toString());

    }//GEN-LAST:event_jTableMouseClicked
    //Method to fill fields if entry selected by up down on keyboard
    private void jTableKeyReleased(java.awt.event.KeyEvent evt)//GEN-FIRST:event_jTableKeyReleased
    {//GEN-HEADEREND:event_jTableKeyReleased
        if (evt.getKeyCode() == KeyEvent.VK_UP
                || evt.getKeyCode() == KeyEvent.VK_DOWN)
        {
            rowIndex = jTable.getSelectedRow();
            //Get info of entry
            jTextField_ID.setText(model.getValueAt(rowIndex, 0).toString());
            jTextField_FName.setText(model.getValueAt(rowIndex, 1).toString());
            jTextField_LName.setText(model.getValueAt(rowIndex, 2).toString());
            jTextField_IDProof.setText(model.getValueAt(rowIndex, 3).toString());
            jTextArea_Address.setText(model.getValueAt(rowIndex, 4).toString());
        }
    }//GEN-LAST:event_jTableKeyReleased
