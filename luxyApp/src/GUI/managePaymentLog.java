    //Method to fill fields if entry selected by up down on keyboard
    private void jTableKeyReleased(java.awt.event.KeyEvent evt)//GEN-FIRST:event_jTableKeyReleased
    {//GEN-HEADEREND:event_jTableKeyReleased
        if (evt.getKeyCode() == KeyEvent.VK_UP
                || evt.getKeyCode() == KeyEvent.VK_DOWN)
        {
            //Get info of entry
            rowIndex = jTable.getSelectedRow();
            jTextField_ID.setText(model.getValueAt(rowIndex, 0).toString());
            jTextField_LeasingID.setText(model.getValueAt(rowIndex, 1).toString());
            jTextField_Status.setText(model.getValueAt(rowIndex, 2).toString());
            jTextField_Type.setText(model.getValueAt(rowIndex, 4).toString());
            jTextField_Check.setText(String.valueOf(model.getValueAt(rowIndex, 5)));
            jTextField_Transact.setText(String.valueOf(model.getValueAt(rowIndex, 6)));
        }
        //Convert date
        Date date;
        try
        {
            date = new SimpleDateFormat("yyyy-MM-dd").parse(model.getValueAt(rowIndex,
                    3).toString());
            jDate_PaymentDate.setDate(date);
          //Handle errors
        } catch (ParseException ex)
        {
            Logger.getLogger(managePaymentLog.class.getName()).log(Level.SEVERE,
                    null, ex);
        }
    }//GEN-LAST:event_jTableKeyReleased
